public class swapping {
   public static void main(String[]args){
                 int num1 = 8;
                 int num2 = 12;
              System.out.println("before swapping");
              System.out.println("num1="+num1);
              System.out.println("num2="+num2);
              int temp;
              temp=num1;
              num1=num2;
              num2=temp;
              System.out.println("after swapping");
              System.out.println("num1="+num1);
              System.out.println("num2="+num2);
   
   }           
              
}
